/// Location of a Parquet file within a namespace's object store.
///
/// There are two currently-valid formats recognized by this code:
///
/// - Partitions with `hash_id`s create `ParquetFilePath`s of the format
///   `{namespace ID}/{table ID}/{hex string partition hash ID}/{Parquet file object store UUID}.parquet`
/// - Partitions without `hash_id`s create `ParquetFilePath`s of the format
///   `{namespace ID}/{table ID}/{partition ID}/{Parquet file object store UUID}.parquet`
///
/// All current partitions have hash IDs, so the only format currently _generated_ by this code
/// uses hash IDs.
///
/// Any change to the object store path format generated by `ParquetFilePath`s will impact the
/// garbage collector and any other code that parses object store paths to interpret them as
/// `ParquetFilePath`s, so care must be taken to ensure any new object store path formats generated
/// by this code are also correctly parsed by this code.
///
/// # Object Store Path
///
/// [`ParquetFilePath`] can be converted to/from [`object_store::path::Path`]:
///
/// ```
/// # use data_types::{NamespaceId, ObjectStoreId, PartitionHashId, PartitionId, PartitionKey,
/// # TableId};
/// # use parquet_file::ParquetFilePath;
/// # use uuid::Uuid;
/// #
/// let table_id = TableId::new(2);
/// let partition_hash_id = PartitionHashId::new(table_id, &PartitionKey::from("arbitrary"));
/// let parquet_path = ParquetFilePath::new(
///     NamespaceId::new(1),
///     table_id,
///     &partition_hash_id,
///     ObjectStoreId::from_uuid(Uuid::nil()),
/// );
///
/// let os_path = parquet_path.object_store_path();
/// assert_eq!(
///     os_path.to_string(),
///     "1/\
///      2/\
///      fb17c3ea5c3987ff9ac38755cc79a6e00c5a2eec96152f5d606557526c278607/\
///      00000000-0000-0000-0000-000000000000.parquet",
/// );
/// ```
#[derive(Debug, Clone, PartialEq, Eq, PartialOrd, Ord, Hash)]
pub struct ParquetFilePath {
    namespace_id: NamespaceId,
    table_id: TableId,
    partition_id: PartitionHashId,
    object_store_id: ObjectStoreId,
}

impl ParquetFilePath {
    /// Create parquet file path relevant for the storage layout.
    pub fn new(
        namespace_id: NamespaceId,
        table_id: TableId,
        partition_id: &PartitionHashId,
        object_store_id: ObjectStoreId,
    ) -> Self {
        Self {
            namespace_id,
            table_id,
            partition_id: partition_id.clone(),
            object_store_id,
        }
    }

    /// Get namespace ID.
    pub fn namespace_id(&self) -> NamespaceId {
        self.namespace_id
    }

    /// Get table ID.
    pub fn table_id(&self) -> TableId {
        self.table_id
    }

    /// Get object-store path.
    pub fn object_store_path(&self) -> Path {
        let Self {
            namespace_id,
            table_id,
            partition_id,
            object_store_id,
        } = self;
        Path::from_iter([
            namespace_id.to_string().as_str(),
            table_id.to_string().as_str(),
            partition_id.to_string().as_str(),
            &format!("{object_store_id}.parquet"),
        ])
    }

    /// Get object store ID.
    pub fn object_store_id(&self) -> ObjectStoreId {
        self.object_store_id
    }

    /// Set new object store ID.
    pub fn with_object_store_id(self, object_store_id: ObjectStoreId) -> Self {
        Self {
            object_store_id,
            ..self
        }
    }

    /// extract file uuid from file path: "database_id/table_id/parition_hash_id/file_uuid.parquet"
    pub fn uuid_from_path(file_path: &Path) -> Option<ObjectStoreId> {
        file_path.parts().nth(3).and_then(|file_uuid| {
            let file_uuid = file_uuid.as_ref();
            // check if ".parquet" is at the end, if so, remove it
            let file_uuid = file_uuid.strip_suffix(".parquet").unwrap_or(file_uuid);
            ObjectStoreId::from_str(file_uuid).ok()
        })
    }
}

impl From<&Self> for ParquetFilePath {
    fn from(borrowed: &Self) -> Self {
        borrowed.clone()
    }
}

impl From<(&PartitionHashId, &crate::metadata::IoxMetadata)> for ParquetFilePath {
    fn from((partition_hash_id, m): (&PartitionHashId, &crate::metadata::IoxMetadata)) -> Self {
        Self {
            namespace_id: m.namespace_id,
            table_id: m.table_id,
            partition_id: partition_hash_id.clone(),
            object_store_id: m.object_store_id,
        }
    }
}

impl From<&ParquetFile> for ParquetFilePath {
    fn from(f: &ParquetFile) -> Self {
        Self {
            namespace_id: f.namespace_id,
            table_id: f.table_id,
            partition_id: f.partition_hash_id.clone(),
            object_store_id: f.object_store_id,
        }
    }
}

impl From<&ParquetFileParams> for ParquetFilePath {
    fn from(f: &ParquetFileParams) -> Self {
        Self {
            namespace_id: f.namespace_id,
            table_id: f.table_id,
            partition_id: f.partition_hash_id.clone(),
            object_store_id: f.object_store_id,
        }
    }
}

impl std::fmt::Display for ParquetFilePath {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        write!(f, "{}", self.object_store_path().as_ref())
    }
}

