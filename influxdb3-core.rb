    boldgreen = "\033[1;32m"
    dim = "\033[2m"
    nc = "\033[0m" # No Color

    <<~EOS
      \n
      ┌───────────────────────────────────────────────────────┐
      │ 🚀 #{boldgreen}InfluxDB 3 Core has been installed and configured!#{nc} │
      ├───────────────────────────────────────────────────────┘
      │
      │ 📁 Config file:     #{etc}/influxdb3/influxdb3.conf
      │ 📁 Data directory:  #{var}/lib/influxdb3
      │ 📁 Logs directory:  #{var}/log/influxdb3
      │
      │ #{bold}Current Configuration:#{nc}
      │ Node ID: node0
      │ Storage: file
